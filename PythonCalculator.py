number1 = int(input("Input first number:"))
arithmetic = input("What arithmatic would you like to use?")
number2 = int(input("Input second number:"))

if arithmetic == "+":
    print(number1 + number2)

elif arithmetic == "-":
    print(number1 - number2)

elif arithmetic == "*":
    print(number1 * number2)

elif arithmetic == "/":
    print(number1 / number2)

